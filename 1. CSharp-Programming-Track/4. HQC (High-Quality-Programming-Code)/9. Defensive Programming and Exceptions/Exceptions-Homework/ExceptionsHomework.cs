using System;
using System.Collections.Generic;
using System.Diagnostics;

public class ExceptionsHomework
{
    public static T[] Subsequence<T>(T[] arr, int startIndex, int count)
    {
        if (arr == null)
        {
            throw new ArgumentNullException("Input array can't be null.");
        }

        if (arr.Length == 0)
        {
            throw new ArgumentException("Array can't be empty");
        }

        if (startIndex < 0 || startIndex >= arr.Length)
        {
            throw new ArgumentOutOfRangeException("Invalid start index.");
        }

        if (startIndex + count > arr.Length)
        {
            throw new ArgumentOutOfRangeException("Count and startindex will cause out of range.");
        }

        List<T> result = new List<T>();
        for (int i = startIndex; i < startIndex + count; i++)
        {
            result.Add(arr[i]);
        }

        Debug.Assert(count == result.Count,"Requested count and delivered count not equal.");

        return result.ToArray();
    }

    public static string ExtractEnding(string str, int count)
    {
        if (count > str.Length)
        {
            throw new ArgumentException("Count can't be greater than string length.");
        }

        return str.Substring(str.Length - count);
    }

    public static bool IsPrime(int number)
    {
        for (int divisor = 2; divisor <= Math.Sqrt(number); divisor++)
        {
            if (number % divisor == 0)
            {
                return false;
            }
        }

        return true;
    }

